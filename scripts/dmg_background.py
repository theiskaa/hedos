SCALE = 2
W, H = 820 * SCALE, 500 * SCALE
CELL = 15


def blend(color, alpha):
    return tuple(round(PAPER[i] + (color[i] - PAPER[i]) * alpha) for i in range(3))


def warm_color(warmth):
    return tuple(round(INK[i] + (AMBER[i] - INK[i]) * warmth) for i in range(3))


def main(out_path):
    img = Image.new("RGB", (W, H), PAPER)
    d = ImageDraw.Draw(img)
    font = ImageFont.truetype("/System/Library/Fonts/Menlo.ttc", int(CELL * 0.94))

    cols = W // CELL + 1
    rows = H // CELL + 1
    koala = [[1 if ch == "1" else 0 for ch in row] for row in KOALA]
    koala_col0 = cols - KOALA_W - 1
    koala_row0 = rows - KOALA_H - 1
    reach = math.hypot(cols, rows) * 0.72

    for r in range(rows):
        for c in range(cols):
            dist = math.hypot(cols - c, rows - r)
            fall = max(0.0, 1.0 - dist / reach) ** 1.35
            th = BAYER[r & 3][c & 3]

            kc = c - koala_col0
            kr = r - koala_row0
            in_koala = 0 <= kr < KOALA_H and 0 <= kc < KOALA_W and koala[kr][kc] == 1

            v = 0.0
            if in_koala and 0.55 + 0.5 * fall > th:
                v = 0.9 + 0.1 * fall
            elif fall > th * 0.82:
                v = 0.28 + fall * 0.72

            if v < 0.08:
                continue

            idx = min(len(RAMP) - 1, int(v * (len(RAMP) - 1)))
            ch = RAMP[max(1, idx)]
            warmth = min(1.0, 0.1 + fall * 0.95 + (0.15 if in_koala else 0.0))
            alpha = min(1.0, 0.3 + v * 0.85)
            d.text((c * CELL, r * CELL), ch, font=font, fill=blend(warm_color(warmth), alpha))

    img.save(out_path, "PNG", dpi=(72 * SCALE, 72 * SCALE))


if __name__ == "__main__":
    main(sys.argv[1])
